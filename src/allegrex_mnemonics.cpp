const char *get_mnemonic_name(allegrex_mnemonic mne)
{
    u32 i = static_cast<u32>(mne);

    if (i >= static_cast<u32>(allegrex_mnemonic::_MAX))
        i = static_cast<u32>(allegrex_mnemonic::_UNKNOWN);

    return mnemonic_names[i];
}
